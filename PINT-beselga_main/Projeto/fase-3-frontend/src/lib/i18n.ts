const LANG_LOCALE: Record<Lang, string> = {
  pt: "pt-PT",
  en: "en-GB",
  es: "es-ES",
};

export function getLangLocale(lang?: Lang): string {
  return LANG_LOCALE[lang ?? getLang()];
}

export function formatDate(date: string | Date, options?: Intl.DateTimeFormatOptions): string {
  const d = typeof date === "string" ? new Date(date) : date;
  return d.toLocaleDateString(getLangLocale(), options);
}

export function formatDateTime(date: string | Date, options?: Intl.DateTimeFormatOptions): string {
  const d = typeof date === "string" ? new Date(date) : date;
  return d.toLocaleString(getLangLocale(), options);
}

export function t(key: string, lang?: Lang): string {
  const l = lang ?? getLang();
  return TRANSLATIONS[l]?.[key] ?? TRANSLATIONS.pt[key] ?? key;
}

export function tOrDefault(key: string, fallback: string, lang?: Lang): string {
  const result = t(key, lang);
  return result === key ? fallback : result;
}
